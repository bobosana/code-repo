/*Here goes our all time fav hello world program*/

public class HelloWorld {
	
	public static void main(String args[]) {
		System.out.println("Hellow world");
	}
	
	/*Adding the add method
		This method adds up two integers passed as parameters and returns the sum.
	*/
	public int add(int a,int b) {
		return a+b;
	}
	
		/*Adding the sub method
		This method subtracts up two integers passed as parameters and returns the difference.
	*/
	public int sub(int a,int b) {
		return a-b;
	}
}
